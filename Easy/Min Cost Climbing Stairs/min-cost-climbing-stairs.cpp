class Solution {
  public:
    int minCostClimbingStairs(vector<int>&cost ,int N) {
        int p1= cost[0], p2 = cost[1];
        for(int i = 2; i < cost.size(); i++)
        {
            int p = min(p1, p2) + cost[i];
            p1 = p2;
            p2 = p;
        }
        
        return min(p1, p2);
    }
};

